import { createHash } from 'node:crypto'
import { readFile, readdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import process from 'node:process'
import { parseDocument } from 'yaml'

// Generates .agents/skills/INDEX.md — a flat, one-line-per-skill catalog derived
// from each SKILL.md frontmatter, so agents can keyword-match a task to a skill
// without opening every SKILL.md. The index is DERIVED, never a source of truth;
// AGENTS.md § Skill Routing remains canonical.
//
// A data hash of (name, description, refs) is embedded as an HTML comment. The
// `--check` mode recomputes the hash from the live skills and compares it to the
// committed index, so a skill added or edited without regenerating fails
// `pnpm agents:validate`. The hash is independent of markdown formatting, so
// `pnpm format:markdown` may reflow the file freely.

const SKILLS_DIRECTORY = '.agents/skills'
const OUTPUT = path.join(SKILLS_DIRECTORY, 'INDEX.md')
const HASH_PATTERN = /<!-- agents:index-hash ([a-f0-9]{64}) -->/

// Parse SKILL.md YAML frontmatter with the same `yaml` parser as
// scripts/validate-agent-skills.mjs (which runs immediately before this in
// `agents:validate`), rather than a second hand-rolled regex parser.
function readFrontmatter(content) {
  const match = content.match(/^---\r?\n([\s\S]*?)\r?\n---(?:\r?\n|$)/)
  if (!match) return {}
  const parsed = parseDocument(match[1])
  if (parsed.errors.length > 0) return {}
  const metadata = parsed.toJS()
  if (!metadata || typeof metadata !== 'object' || Array.isArray(metadata)) return {}
  return metadata
}

async function collectSkills() {
  const entries = (await readdir(SKILLS_DIRECTORY, { withFileTypes: true }))
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort((left, right) => left.localeCompare(right))

  const rows = []
  for (const slug of entries) {
    let content
    try {
      content = await readFile(path.join(SKILLS_DIRECTORY, slug, 'SKILL.md'), 'utf8')
    } catch {
      continue
    }
    const metadata = readFrontmatter(content)
    let refs = 0
    try {
      refs = (await readdir(path.join(SKILLS_DIRECTORY, slug, 'references'))).filter((file) =>
        file.endsWith('.md'),
      ).length
    } catch {
      refs = 0
    }
    rows.push({
      name: typeof metadata.name === 'string' ? metadata.name : slug,
      description: typeof metadata.description === 'string' ? metadata.description : '',
      refs,
    })
  }
  return rows
}

function dataHash(rows) {
  const serialized = rows.map((row) => `${row.name}\t${row.description}\t${row.refs}`).join('\n')
  return createHash('sha256').update(serialized).digest('hex')
}

function render(rows, hash) {
  const escape = (text) => text.replace(/\|/g, '\\|')
  const lines = [
    `<!-- agents:index-hash ${hash} -->`,
    '<!-- Generated by scripts/build-skill-index.mjs — do not edit by hand. Run `pnpm agents:index`. -->',
    '',
    '# Agent Skill Index',
    '',
    'Flat catalog of `.agents/skills/`, one line per skill, for fast keyword lookup before opening a `SKILL.md`.',
    'Derived from frontmatter — **not** a source of truth; `AGENTS.md` § Skill Routing stays canonical.',
    `Regenerate with \`pnpm agents:index\` (staleness is enforced by \`pnpm agents:validate\`).`,
    '',
    `${rows.length} skills. \`[refs:N]\` marks skills with N \`references/*.md\` deep-dive files.`,
    '',
  ]
  for (const row of rows) {
    const refs = row.refs > 0 ? ` [refs:${row.refs}]` : ''
    lines.push(`- \`${row.name}\`${refs} — ${escape(row.description)}`)
  }
  lines.push('')
  return lines.join('\n')
}

async function main() {
  const check = process.argv.includes('--check')
  const rows = await collectSkills()
  const hash = dataHash(rows)

  if (!check) {
    await writeFile(OUTPUT, render(rows, hash))
    console.log(`Wrote ${OUTPUT} (${rows.length} skills).`)
    return
  }

  let committed
  try {
    committed = await readFile(OUTPUT, 'utf8')
  } catch {
    console.error(`ERROR: ${OUTPUT} is missing. Run \`pnpm agents:index\`.`)
    process.exitCode = 1
    return
  }
  const match = committed.match(HASH_PATTERN)
  if (!match) {
    console.error(`ERROR: ${OUTPUT} has no index hash. Run \`pnpm agents:index\`.`)
    process.exitCode = 1
    return
  }
  if (match[1] !== hash) {
    console.error(
      `ERROR: ${OUTPUT} is stale (live skills hash ${hash.slice(0, 12)}…). Run \`pnpm agents:index\`.`,
    )
    process.exitCode = 1
    return
  }
  console.log(`Skill index up to date (${rows.length} skills).`)
}

await main()
